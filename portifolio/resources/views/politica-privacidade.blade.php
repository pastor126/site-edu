<h2>Política de Privacidade para Aplicativos</h2>
<p>Esta política de privacidade descreve como os aplicativos relacionados abaixo coletam, usam, armazenam e protege as informações dos usuários. Ao utilizar qualquer destes aplicativos, você concorda com os termos desta política.</p>

