<p>Para anunciantes e redes de publicidade e empresas de análise de terceiros, conforme descrito na seção abaixo</p>

<h5>Coleta de informações para publicidade</h5>

<p>Trabalhamos com empresas de análise para nos ajudar a entender como o Aplicativo está sendo usado, como a frequência e a duração de uso. Trabalhamos com anunciantes e redes de publicidade de terceiros, que precisam saber como você interage com as publicidades fornecidas ajudando assim manter estes aplicativos gratuitos ou com valores bastante acessíveis.</p>

<p>Informações coletadas pelo Aplicativo para publicidade, incluindo, mas não se limitando, o ID de identificação exclusivo do seu dispositivo móvel e seu número de telefone celular. Para proteger o anonimato dessas informações, usamos uma tecnologia de criptografia para ajudar a garantir que esses terceiros não possam identificá-lo pessoalmente. Esses terceiros também podem obter informações anônimas sobre outros aplicativos que você baixou para seu dispositivo móvel, os sites para celular que você visita, suas informações de localização não precisas (por exemplo, seu CEP) e outras informações de localização não precisas para ajudar a analisar e servir publicidade direcionada anônima no aplicativo e em outros lugares.</p>

<h5>Dos direitos de exclusão</h5>

<p>Desative todas as coletas de informações desinstalando o Aplicativo:  Você pode parar toda a coleta de informações pelo Aplicativo facilmente, desinstalando o Aplicativo. Você pode usar os processos de desinstalação padrão que podem estar disponíveis como parte de seu dispositivo móvel ou via mercado ou rede de aplicativos móveis. </p>

<h5>Segurança</h5>

<p>Estamos preocupados em proteger a confidencialidade de suas informações. Fornecemos proteções físicas, eletrônicas e processuais para proteger as informações que processamos e mantemos. Por favor, esteja ciente de que, embora nos empenhemos em fornecer segurança razoável para as informações que processamos e mantemos, nenhum sistema de segurança pode evitar todas as possíveis violações de segurança.</p>

<h5>Alterações e Consentimento</h5>

<p>Podemos atualizar esta política de privacidade periodicamente. Quando isso acontecer, as alterações serão publicadas nesta página com a data da última atualização. Recomendamos que você reveja esta política regularmente para estar ciente de quaisquer modificações.</p>

