<p>Aplicativos de autoria de Eduardo Chagas Pacheco</p>

<p>Informações fornecidas pelo usuário</p>

<p>As informações inseridas pelo usuário, como datas e horários (exceto dados de cadastro de conta caso exista), são armazenadas localmente no dispositivo do usuário e não são compartilhadas com terceiros ou servidores externos. Elas são usadas apenas para o funcionamento correto do aplicativo e para fornecer uma experiência personalizada ao usuário.</p>

<h5>Coleta automática de informações</h5>

<p>Informações como estatísticas de instalação e utilização são coletadas para relatórios estratégicos, sem identificação pessoal.</p>

<h5>Coleta de informações de localização em tempo real</h5>

<p>Este aplicativo não faz a coleta de informações de localização em tempo real.</p>

<h5>Acesso de informações por terceiros</h5>

